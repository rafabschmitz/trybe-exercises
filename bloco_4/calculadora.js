let operacao = "%";
let a = 15;
let b = 5;

switch(operacao){
    case "+":
    console.log(a+b);
    break;

    case "-":
    console.log(a-b);
    break;

    case "*":
    console.log(a*b);
    break;

    case "/":
    console.log(a/b);
    break;

    case "%":
    console.log(a%b);
    break;

    default:
    console.log("Operação desconhecida")
}